public class AgentResult {
    private Integer year;
    private Integer breedCCount;
    private Integer breedNCCount;
    private Integer breedCLostCount;
    private Integer breedCGainedCount;
    private Integer breedRegained;

    public Integer getYear() {
        return year;
    }

    public void setYear(Integer year) {
        this.year = year;
    }

    public Integer getBreedCCount() {
        return breedCCount;
    }

    public void setBreedCCount(Integer breedCCount) {
        this.breedCCount = breedCCount;
    }

    public Integer getBreedNCCount() {
        return breedNCCount;
    }

    public void setBreedNCCount(Integer breedNCCount) {
        this.breedNCCount = breedNCCount;
    }

    public Integer getBreedCLostCount() {
        return breedCLostCount;
    }

    public void setBreedCLostCount(Integer breedCLostCount) {
        this.breedCLostCount = breedCLostCount;
    }

    public Integer getBreedCGainedCount() {
        return breedCGainedCount;
    }

    public void setBreedCGainedCount(Integer breedCGainedCount) {
        this.breedCGainedCount = breedCGainedCount;
    }

    public Integer getBreedRegained() {
        return breedRegained;
    }

    public void setBreedRegained(Integer breedRegained) {
        this.breedRegained = breedRegained;
    }
}
